package com.android;

public class Main {

	public static void main(String[] args) {
		
		//INNER CLASSES
		
		/*
		 * A nested inner class is a class within a class. 
		 * Of which the are 4 types:
		 * + A MEMBER inner classs. Defined at the same level as instance vars, and is NOT static.
		 * + A LOCAL inner class. This is a class that is defined inside a method. 
		 * + An anonymous inner class. An inner class which doesn't have a name. 
		 * + A static inner class. Which is defined at the same level as a static variable.
		 * 
		 * Classes in this project:
		 * Animal class - for member inner class
		 * Human class for Local inner class
		 * Building class for anonymous inner class
		 * Doctor for static inner classes
		 */
	
		System.out.println("Inner classes");
	}

}
